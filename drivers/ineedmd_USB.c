{
  ERROR_CODE eEC = ER_FAIL;

  eEC = eUSB_setup();
  if(eEC == ER_FAIL)
  {
    vDEBUG("vUSB_task SYS HALT, USB setup failure!");
    while(1){};
  }else{/*do nothing*/}

  while(1)
  {
    Task_sleep(1000);
  }
}

